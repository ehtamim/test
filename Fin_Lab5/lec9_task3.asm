.MODEL SMALL
.STACK 100H
.DATA
REV DB 'BEFORE REVERSE: $'
AREV DB 0AH,0DH,'AFTER REVERSE: $'
STRING DB '123$'
.CODE

MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    
    LEA DX,REV
    MOV AH,9
    INT 21H
    LEA DX,STRING
    MOV AH,9
    INT 21H
    
    MOV SI,OFFSET STRING
    MOV CX,3
L1:
    MOV BX,[SI]
    PUSH BX
    INC SI
    LOOP L1

    LEA DX,AREV
    MOV AH,9
    INT 21H
    
    MOV CX,3
L2:
    POP DX
    MOV AH,2
    INT 21H
    LOOP L2

EXIT:
MOV AH,4CH
INT 21H
MAIN ENDP
END  MAIN